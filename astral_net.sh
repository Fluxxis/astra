#!/usr/bin/env bash
export NOCOLOR="${NOCOLOR:-}"
set -Eeuo pipefail

APP_NAME="Astral Net"
INSTALL_DIR="${APP_DIR:-$PWD/astral-net}"
PAYLOAD_START="__ASTRAL_NET_PAYLOAD_BELOW__"

say() { printf '[1;37m[Astral Net][0m %s
' "$*"; }
fail() { printf '[1;31m[Astral Net] ОШИБКА:[0m %s
' "$*" >&2; exit 1; }
need_cmd() { command -v "$1" >/dev/null 2>&1; }

extract_payload() {
  say "создаю проект в $INSTALL_DIR"
  local backup_dir=""
  if [[ -d "$INSTALL_DIR/data" ]]; then
    backup_dir="$(mktemp -d /tmp/astral-net-data.XXXXXX)"
    mv "$INSTALL_DIR/data" "$backup_dir/data"
    say "сохранил старую базу и медиа"
  fi
  rm -rf "$INSTALL_DIR"
  mkdir -p "$INSTALL_DIR"
  local tmp_tar
  tmp_tar="$(mktemp /tmp/astral-net.XXXXXX.tar.gz)"
  awk "/^$PAYLOAD_START$/ {found=1; next} found {print}" "$0" | base64 -d > "$tmp_tar" || fail "не смог распаковать встроенный проект"
  tar -xzf "$tmp_tar" -C "$INSTALL_DIR" || fail "не смог извлечь файлы проекта"
  rm -f "$tmp_tar"
  if [[ -n "$backup_dir" && -d "$backup_dir/data" ]]; then
    rm -rf "$INSTALL_DIR/data"
    mv "$backup_dir/data" "$INSTALL_DIR/data"
    rmdir "$backup_dir" 2>/dev/null || true
    say "вернул старую базу и медиа"
  fi
  chmod +x "$INSTALL_DIR/run_messenger.sh" || true
}

main() {
  if ! need_cmd base64; then fail "нет команды base64. Поставь coreutils"; fi
  if ! need_cmd tar; then fail "нет команды tar"; fi
  extract_payload
  if [[ "${1:-}" == "--extract-only" || "${ASTRAL_EXTRACT_ONLY:-0}" == "1" ]]; then
    say "файлы готовы. Запуск: cd $INSTALL_DIR && ./run_messenger.sh"
    exit 0
  fi
  say "запускаю Astral Net"
  cd "$INSTALL_DIR"
  exec ./run_messenger.sh
}

main "$@"
exit 0
